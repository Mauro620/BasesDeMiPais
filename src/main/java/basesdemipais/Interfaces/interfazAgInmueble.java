    private void jbbuscarpropietarioActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jbbuscarpropietarioActionPerformed
        // TODO add your handling code here:
        if (!jtfidpropietario.getText().equals("")) {

            int id = Integer.parseInt(jtfidpropietario.getText());
            if (basesdemipais.Interfaces.interfazInicio.getPropietarios().Buscar(id) != null) {
                jtfidpropietario.setEnabled(false);
                jtftamano.setEnabled(true);
                jtfdireccion.setEnabled(true);
                jcbtipoinmueble.setEnabled(true);
                jcbzona.setEnabled(true);
                jbcontinuar.setEnabled(true);
                JOptionPane.showMessageDialog(null,
                        basesdemipais.Interfaces.interfazInicio.getPropietarios()
                                .Buscar(Integer.parseInt(jtfidpropietario.getText())));

                // Agregar funcion para asignar codigo automatico
                jtfcodigo.setText(String.valueOf(
                        basesdemipais.Interfaces.interfazInicio.getInmuebles().codigo()));
            } else {
                JOptionPane.showMessageDialog(null, "El usuario no ha sido encontrado, por favor verifique que exista.");
            }

        }
    }// GEN-LAST:event_jbbuscarpropietarioActionPerformed

