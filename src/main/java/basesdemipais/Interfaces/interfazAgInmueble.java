    private void jbcontinuarActionPerformed(java.awt.event.ActionEvent evt) {

        boolean uwu = true;
        if (jcbtipoinmueble.getSelectedItem().equals("Seleccione")) {
            uwu = false;
            jcbtipoinmueble.setBackground(Color.RED);
        }
        if (jcbzona.getSelectedItem().equals("Seleccione")) {
            uwu = false;
            jcbzona.setBackground(Color.RED);
        }
        if (jtftamano.getText().equals("")) {
            uwu = false;
            jtftamano.setBackground(new java.awt.Color(255, 204, 204));
        }
        if (jtfdireccion.getText().equals("")) {
            uwu = false;
            jtfdireccion.setBackground(new java.awt.Color(255, 204, 204));
        }
        if (jcbzona.getSelectedItem().equals("Selecciona")) {
            uwu = false;
            jcbzona.setBackground(new java.awt.Color(255, 204, 204));
        }
        if (uwu == false) {
            JOptionPane.showMessageDialog(null, "Datos invalidos o faltantes");
        }
        if (uwu == true) {
            if (jcbtipoinmueble.getSelectedIndex() == 1) {
                codigo = Integer.parseInt(jtfcodigo.getText());
                tipo = String.valueOf(jcbtipoinmueble.getSelectedItem());
                p = basesdemipais.Interfaces.interfazInicio.getPropietarios()
                        .Buscar(Integer.parseInt(jtfidpropietario.getText()));
                tamano = Double.parseDouble(jtftamano.getText());
                zona = String.valueOf(jcbzona.getSelectedItem());
                direccion = jtfdireccion.getText();
                precio = vector.defPrecio(zona, tamano, tipo);

                interfazApartamento apto = new interfazApartamento();
                apto.setVisible(true);
                this.setVisible(false);

            } else if (jcbtipoinmueble.getSelectedIndex() == 2) {
                codigo = Integer.parseInt(jtfcodigo.getText());
                tipo = String.valueOf(jcbtipoinmueble.getSelectedItem());
                p = basesdemipais.Interfaces.interfazInicio.getPropietarios()
                        .Buscar(Integer.parseInt(jtfidpropietario.getText()));
                tamano = Double.parseDouble(jtftamano.getText());
                zona = String.valueOf(jcbzona.getSelectedItem());
                direccion = jtfdireccion.getText();
                precio = vector.defPrecio(zona, tamano, tipo);

                interfazParqueadero parqueadero = new interfazParqueadero();
                parqueadero.setVisible(true);
                this.setVisible(false);

            } else if (jcbtipoinmueble.getSelectedIndex() == 3) {
                codigo = Integer.parseInt(jtfcodigo.getText());
                tipo = String.valueOf(jcbtipoinmueble.getSelectedItem());
                p = basesdemipais.Interfaces.interfazInicio.getPropietarios()
                        .Buscar(Integer.parseInt(jtfidpropietario.getText()));
                tamano = Double.parseDouble(jtftamano.getText());
                zona = String.valueOf(jcbzona.getSelectedItem());
                direccion = jtfdireccion.getText();
                precio = vector.defPrecio(zona, tamano, tipo);

                interfazLocal local = new interfazLocal();
                local.setVisible(true);
                this.setVisible(false);
            }
        }
    }// GEN-LAST:event_jbcontinuarActionPerformed

