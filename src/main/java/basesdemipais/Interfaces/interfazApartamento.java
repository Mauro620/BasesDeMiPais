        public static String Mostrar(String[] vector) {
                if (vector == null || vector.length == 0) {
                        return ""; // Devuelve una cadena vacía si el vector es nulo o está vacío.
                }

                StringBuilder resultado = new StringBuilder();

                for (String elemento : vector) {
                        resultado.append(elemento);
                        resultado.append(" "); // Agregamos un espacio entre cada elemento
                }

                return resultado.toString();
        }

