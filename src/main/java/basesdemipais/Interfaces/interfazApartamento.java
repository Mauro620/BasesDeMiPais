        private void jbguardarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jbguardarActionPerformed
                // TODO add your handling code here:

                boolean comp = true;
                if (jtfnumerodehabitaciones.getText().equals("")) {
                        comp = false;
                        jtfnumerodehabitaciones.setBackground(new java.awt.Color(255, 204, 204));
                }
                if (jtfnumerodearmarios.getText().equals("")) {
                        comp = false;
                        jtfnumerodearmarios.setBackground(new java.awt.Color(255, 204, 204));
                }
                if (jtfnumerodebanos.getText().equals("")) {
                        comp = false;
                        jtfnumerodebanos.setBackground(new java.awt.Color(255, 204, 204));
                }

                if (jcbtipodecocina.getSelectedItem().equals("Seleccione")) {
                        comp = false;
                        jcbtipodecocina.setBackground(new java.awt.Color(255, 0, 51));
                }

                if (jcbtipodepiso.getSelectedItem().equals("Seleccione")) {
                        comp = false;
                        jcbtipodepiso.setBackground(new java.awt.Color(255, 0, 51));
                }

                if (comp == false) {
                        JOptionPane.showMessageDialog(null, "Datos insuficientes o invalidos");
                }

                if (comp) {
                        int numeroHabitaciones = Integer.parseInt(jtfnumerodehabitaciones.getText());

                        int numeroArmarios = Integer.parseInt(jtfnumerodearmarios.getText());
                        int numeroBanos = Integer.parseInt(jtfnumerodebanos.getText());
                        String tipoCocina = (String) jcbtipodecocina.getSelectedItem();
                        String tipoPiso = (String) jcbtipodepiso.getSelectedItem();
                        // FALTA AGREGAR A VEC///

                        
                        interfazAgInmueble claseprueba = new interfazAgInmueble();
                        apartamento apto = new apartamento(claseprueba.codigo, claseprueba.p, claseprueba.tamano, claseprueba.direccion, claseprueba.zona, claseprueba.precio, numeroHabitaciones, numeroArmarios, numeroBanos,tipoPiso, tipoCocina);
                        
                        if(basesdemipais.Interfaces.interfazInicio.getInmuebles().Adicionar(apto)){
                            JOptionPane.showMessageDialog(null, "Inmueble creado");
                            basesdemipais.Interfaces.interfazInicio.getObjInicio().setVisible(true);
                            this.setVisible(false);
                        }
                        
                        

                }
        }// GEN-LAST:event_jbguardarActionPerformed

